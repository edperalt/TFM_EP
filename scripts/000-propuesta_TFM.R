Se propone un TFM basado en un concurso realizado en la plataforma  XEEK (https://xeek.ai/challenges) . En el año 2020 realizaron un concurso para atacar un problema recurrente en la industria del petróleo, la predicción de Litologia (tipos de roca) en pozos a partir de la interpretación de registros. Por razones económicas y operacionales normalmente en muy pocos pozos se extraen muestras de roca donde se conoce a ciencia cierta la litología, y  estos se utilizan para guiar las interpretaciones en los pozos vecinos.  Este tipo de interpretación aun hoy es un ejercicio predominantemente manual.   
Actualmente existe una gran resistencia en nuestra industria a las tecnologías nuevas, sin embargo la velocidad a la que generamos datos y  las crecientes necesidades energéticas, así como la mejoría de la capacidad computacional, cada vez mas nos abre la ventana para empezar a utilizar este tipo de herramientas en la industria.
Este proyecto representa el ejercicio que realiza un geologo cuando esta estudiando un yacimiento, y es un proceso que se debe actualizar con cada pozo perforado. Actualmente trabajo en varios campos  el que se perforan un par de pozos al mes, y este tipo de tecnologías pueden ser d egran ayuda para tener primeras interpretaciones prácticamente en tiempo real.

